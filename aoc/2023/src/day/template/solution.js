/**
 * @typedef {any} Parsed
 */

/**
 * @param {Parsed} input 
 * @returns any
 */
const partOne = (input) => {
  return input
};

const partTwo = (input) => {
  return input
};

/**
 * @param {string} input 
 * @returns Parsed
 */
const parse = input => {
  return input
}

/**
 * @param {string} input
 * @returns [any, any]
 */
export default function solver(input) {
  const parsed = parse(input)
  return [partOne(parsed), partTwo(parsed)];
}
